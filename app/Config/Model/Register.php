/**
 * beforeValidate method
 *
 * @param $options array
 * @return void
 */
	public function beforeValidate($options = array()){
		parent::beforeValidate($options);

		if(isset($this->data[$this->alias]['species'])){

			if(strtoupper($this->data[$this->alias]['species'])=='B')
				$this->data[$this->alias]['species']='bovino';
				
			elseif(strtoupper($this->data[$this->alias]['species'])=='CO')
				$this->data[$this->alias]['species']='conejo';

			elseif(strtoupper($this->data[$this->alias]['species'])=='C' || strtoupper($this->data[$this->alias]['species'])=='CA')
				$this->data[$this->alias]['species']='caprino';

			elseif(strtoupper($this->data[$this->alias]['species'])=='O')
				$this->data[$this->alias]['species']='ovino';

			elseif(strtoupper( $this->data[$this->alias]['species'])=='P')
				$this->data[$this->alias]['species']='porcino';

			elseif(strtoupper( $this->data[$this->alias]['species'])=='A')
				$this->data[$this->alias]['species']='aves';

		}
	}	
